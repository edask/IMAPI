package imapi;

public class ComparisonResult {
	
	  boolean breakMethod=false;
	 
	  Double comparisonRes=0.0;

	public boolean isBreakMethod() {
		return breakMethod;
	}

	public void setBreakMethod(boolean breakMethod) {
		this.breakMethod = breakMethod;
	}

	public Double getComparisonRes() {
		return comparisonRes;
	}

	public void setComparisonRes(Double comparisonRes) {
		this.comparisonRes = comparisonRes;
	}
	

}





